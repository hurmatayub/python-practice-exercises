# Ex:1. Addition and Subtraction
num1 = 12
num2 = 8
print(f"Addition: {num1} + {num2} = {num1 + num2}")  # Addition
print(f"Subtraction: {num1} - {num2} = {num1 - num2}")  # Subtraction

# Ex:2. Multiplication Table
n = 7
print(f"\nMultiplication Table of {n}:")
for i in range(1, 11):
    print(f"{n} x {i} = {n * i}")

# Ex:3. Square of a number
num = 5
print(f"\nSquare of {num} is {num ** 2}")

# Ex:4. Finding Remainder
num = 17
divisor = 5
print(f"\nRemainder of {num} divided by {divisor} is {num % divisor}")

# Ex:5. Exponentiation
base = 3
exp = 4
print(f"\n{base} raised to the power of {exp} is {base ** exp}")

# Ex:6. Division and Floor Division
num1 = 50
num2 = 4
print(f"\nDivision: {num1} / {num2} = {num1 / num2}")
print(f"Floor Division: {num1} // {num2} = {num1 // num2}")

# Ex:7. Modulo Operation
num = 26
divisor = 7
print(f"\nRemainder of {num} divided by {divisor} is {num % divisor}")
