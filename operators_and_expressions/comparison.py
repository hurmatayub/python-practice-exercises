# Ex:1. Check Equality
num1, num2 = 10, 15
print(f"Is {num1} equal to {num1}? {num1 == num1}")  # True

num1, num2 = 10, 10
print(f"Is {num1} equal to {num2}? {num1 == num2}")  # True

print("\n")  # Line Break for better readability

# Ex:2. Check Not Equal
num1, num2 = 7, 7
print(f"Is {num1} not equal to {num2}? {num1 != num2}")  # False

num1, num2 = 7, 8
print(f"Is {num1} not equal to {num2}? {num1 != num2}")  # True

print("\n")

# Ex:3. Check Greater Than
num1, num2 = 20, 15
print(f"Is {num1} greater than {num2}? {num1 > num2}")  # True

# Ex:4. Check Less Than
num1, num2 = 10, 25
print(f"Is {num1} less than {num2}? {num1 < num2}")  # True

print("\n")

# Ex:5. Check Greater Than or Equal to
num1, num2 = 30, 30
print(f"Is {num1} greater than or equal to {num2}? {num1 >= num2}")  # True

# Ex:6. Check Less Than or Equal to
num1, num2 = 18, 25
print(f"Is {num1} less than or equal to {num2}? {num1 <= num2}")  # True
